private void loadOrderHistoryeDetails(String CustomerId) {
        try {

            CustometHistoryorderList = OrderDAO.getCustomerOrderHistory(CustomerId);
            if (CustometHistoryorderList.isEmpty()) {
                JOptionPane.showMessageDialog(null, "No orders history ");
                jtDeliveredProductTable.setEnabled(false);

                return;

            }
            int i = 0;
            Object[] rows = new Object[7];
            while (i < CustometHistoryorderList.size()) {
                OrderPojo ord = CustometHistoryorderList.get(i);
                rows[0] = ord.getOrderId();

                rows[1] = ord.getProductName();
                rows[2] = ord.getProductPrice();
                rows[3] = ord.getDeliveryStaffName();
                rows[4] = ord.getCompanyName();
                rows[5] = ord.getCustomerAddress();
                rows[6] = ord.getReview();

                model.addRow(rows);
                i++;

            }

        } catch (SQLException sq) {
            JOptionPane.showMessageDialog(null, "DB ERROR IN View ORder FRAME ");
            sq.printStackTrace();

        }
    }

