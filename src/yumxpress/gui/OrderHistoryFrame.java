    private void jtDeliveredProductTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtDeliveredProductTableMouseClicked
         int selectedRow = jtDeliveredProductTable.getSelectedRow();
        if (selectedRow != -1) {
            String orderId = (String) model.getValueAt(selectedRow, 0);
            String currentReview = (String) model.getValueAt(selectedRow, 6);

            // Display an input dialog for entering a review
            String newReview = JOptionPane.showInputDialog(this, "Enter a new review:", currentReview);

            // Update the review in the table and database
            if (newReview != null) {
                model.setValueAt(newReview, selectedRow, 6);
                try {
                    OrderDAO.Updatereview(orderId, newReview);
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "DB ERROR IN View ORder FRAME ");
                    ex.printStackTrace();

                }
            }
        }
    }//GEN-LAST:event_jtDeliveredProductTableMouseClicked

